#!/usr/bin/env rustc

//-------------------------------------------------------------------
fn main()
{
    print_number(5);
    print_sum(20, 30);
}

//-------------------------------------------------------------------
fn print_number(x : i32 ) 
{
    println!(" x is : {}", x);
}

//-------------------------------------------------------------------
fn print_sum(x: i32, y: i32)
{
    println!("Sum of {} & {} is: {}", x, y, (x+y));
}
